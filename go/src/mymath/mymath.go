// Naming this package
package mymath

// Go is strongly typed (Unlike Python, and like C). Notice types are declared after variable names.
// The return type is declared after parameters are specified.

// defining sum function
func Sum(x int, y int) int {
	return x + y
}

// defining diff function
func Diff(x int, y int) int {
	return x - y
}

// defining diff function
func Mul(x int, y int) int {
	return x - y
}

// defining diff function
func Div(x float64, y float64) float64 {
  return (x / y)
}
